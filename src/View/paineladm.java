public Boolean campo_vazio() {
         boolean status;
        if (Usuario.getText().trim().equals("") || senha.getText().trim().equals("")  ){
            JOptionPane.showMessageDialog(this, "campo em branco");
           return status = false;
          
        } 
        else {
           return status = true;
        }
       
    
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        boolean s = campo_vazio();

        if (s = true) {
            Login p = new Login();
            BD dao = new BD();

            p.setUser(Usuario.getText());
            p.setSenha(Integer.parseInt(senha.getText()));
            p.setTipo(Tipo.getSelectedItem().toString());

            if (p.getTipo().equals("user")) {
                dao.createlogin(p);
                BdPermissoes bd = new BdPermissoes();
                int id = bd.id(p.getUser());
                bd.createPermissoes(id, p.getUser());
                Usuario.setText("");
                senha.setText("");

                readJTable();
            } else {
                dao.createlogin(p);
            }

            Usuario.setText("");
            senha.setText("");

            readJTable();


        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

//        System.out.println("Linha selecionada: "+jTProdutos.getSelectedRow());
        if (jTProdutos.getSelectedRow() != -1) {

            Login p = new Login();
            BD dao = new BD();
            BdPermissoes d = new BdPermissoes();
            p.setId((int) jTProdutos.getValueAt(jTProdutos.getSelectedRow(), 0));
              d.delete(p);
            dao.delete(p);
          

           

            Usuario.setText("");
            senha.setText("");

            readJTable();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir.");
        }


    }//GEN-LAST:event_jButton2ActionPerformed

