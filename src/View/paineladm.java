    public void readJTable() {

        DefaultTableModel modelo = (DefaultTableModel) jTProdutos.getModel();
        modelo.setNumRows(0);

        BD pdao = new BD();

        for (Login v : pdao.readlogin()) {

            modelo.addRow(new Object[]{
                 v.getId(),
                v.getUser(),
                v.getSenha(),
                v.getTipo()

            });
 
        }

    }

    public void readJTableForDesc(String desc) {

        DefaultTableModel modelo = (DefaultTableModel) jTProdutos.getModel();
        modelo.setNumRows(0);

        BD pdao = new BD();
 
        for (Login v : pdao.readloginbusca(desc)) {

            modelo.addRow(new Object[]{
                v.getId(),
                v.getUser(),
                v.getSenha(),
                v.getTipo()

            });
 
        }

    }

