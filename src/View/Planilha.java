    public void CalculaTotal() {
        double a = Double.parseDouble(beneficios.getText());
        double b = Double.parseDouble(encargos.getText());
        double c = Double.parseDouble(SalarioMensal.getText());
        double tot = calculaTot(a, b, c);

        tot *= 100;
        tot = Math.round(tot);
        tot /= 100;

        ctotal.setText(String.valueOf(tot));
    }
