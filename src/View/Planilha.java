    public void CalculaBeneficio() {
        double a = Double.parseDouble(valtransporte.getText());
        double b = Double.parseDouble(cestabasica.getText());
         double c = Double.parseDouble(covmedico.getText());
        double tot = calculabene(a, b, c);
        tot *= 100;
        tot = Math.round(tot);
        tot /= 100;
        beneficios.setText(String.valueOf(tot));
    }
