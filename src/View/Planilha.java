    public Boolean campo_vazio() {
        boolean status;
        if (Funcionario.getText().trim().equals("") || admissao.getText().trim().equals("") || setor.getText().trim().equals("") || cargo.getText().trim().equals("") || salario.getText().trim().equals("") || SalarioMensal.getText().trim().equals("")
                || encargos.getText().trim().equals("") || covmedico.getText().trim().equals("") || seis.getText().trim().equals("") || valtransporte.getText().trim().equals("") || cestabasica.getText().trim().equals("") || beneficios.getText().trim().equals("") || ctotal.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "campo em branco");
            status = false;
            
        } else {
            status = true;
        }
        return status;
    }

