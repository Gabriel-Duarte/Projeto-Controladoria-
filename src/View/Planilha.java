    public void readJTable() {

        DefaultTableModel modelo = (DefaultTableModel) jTProdutos.getModel();
        modelo.setNumRows(0);
        BD pdao = new BD();

        for (Funcionario p : pdao.read()) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getFunc(),
                p.getAdmicao(),
                p.getSetor(),
                p.getCargo(),
                p.getSalario(),
                p.getSalariomensal(),
                p.getEncargos(),
                p.getConvmed(),
                p.getSeis(),
                p.getValetrans(),
                p.getCestaBasica(),
                p.getBeneficio(),
                p.getCustot(),});

        }

    }

