    private void AtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AtualizarActionPerformed
        // TODO add your handling code here:
        boolean s = campo_vazio();

        if (s = true) {
            if (jTProdutos.getSelectedRow() != -1) {

                Funcionario p = new Funcionario();
                System.out.print(p.getEncargos());
                BD dao = new BD();

                p.setFunc(Funcionario.getText());
                p.setAdmicao(admissao.getText());
                p.setSetor(setor.getText().toLowerCase());
                p.setCargo(cargo.getText());
                p.setSalario(Double.parseDouble(salario.getText()));
                p.setSalariomensal(Double.parseDouble(SalarioMensal.getText()));
                p.setEncargos(Double.parseDouble(encargos.getText()));
                p.setConvmed(Double.parseDouble(covmedico.getText()));
                p.setSeis(Double.parseDouble(seis.getText()));
                p.setValetrans(Double.parseDouble(valtransporte.getText()));
                p.setCestaBasica(Double.parseDouble(cestabasica.getText()));
                p.setBeneficio(Double.parseDouble(beneficios.getText()));

                p.setCustot(Double.parseDouble(ctotal.getText()));

                p.setId((int) jTProdutos.getValueAt(jTProdutos.getSelectedRow(), 0));
                dao.update(p);

                apagar();

                readJTable();

            }
        }
    }//GEN-LAST:event_AtualizarActionPerformed

